package j33_Lambda;

public class SeedMethods {
    public static void intYazdir(int a) { // seed- tohum method
        System.out.print(a + " ");
    }

    public static boolean ciftMi(int a) {
        return a % 2 == 0;
    }

    public static int kareAl(int a) {
        return a * a;
    }

    public static int minBul(int a, int b) {
        return a < b ? a : b;
    }

    public static boolean tekMi(int a) {
        return a % 2 == 1;
    }

    public static void strYazdir(String a) { // seed- tohum method
        System.out.print(a + " ");
    }


}
